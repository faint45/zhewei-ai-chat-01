def kb_doc_id(prefix: str, text: str) -> str:
    h = hashlib.sha1(text.encode("utf-8", errors="ignore")).hexdigest()[:16]
    return f"{prefix}_{h}"


def batch_learn(entries: list[dict], source: str, prefix: str = "tw") -> dict:
    coll = get_collection()
    total = len(entries)
    written = 0
    skipped = 0
    errors = 0
    for i, entry in enumerate(entries):
        q = (entry.get("q") or "").strip()
        a = (entry.get("a") or "").strip()
        if not q or not a:
            skipped += 1
            continue
        doc_id = kb_doc_id(prefix, q + a)
        try:
            got = coll.get(ids=[doc_id], include=[])
            if got.get("ids"):
                skipped += 1
                continue
        except Exception:
            pass
        try:
            doc = f"Q: {q}\nA: {a}"
            emb = ollama_embed(doc)
            coll.add(
                documents=[doc],
                metadatas=[{
                    "question": q[:200],
                    "source": source,
                    "time": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
                }],
                ids=[doc_id],
                embeddings=[emb],
            )
            written += 1
            if (i + 1) % 20 == 0:
                print(f"  [{source}] {i+1}/{total} written={written}")
        except Exception as e:
            errors += 1
            if errors <= 3:
                print(f"  [WARN] {e}")
    print(f"  [{source}] å®Œæˆ: total={total} written={written} skipped={skipped} errors={errors}")
    return {"total": total, "written": written, "skipped": skipped, "errors": errors}


